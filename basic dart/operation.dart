void main(){
  // Arithmatic Operator
  var a = 10;
  var b = 20;
  print ("Addition = ${a+b}");
  print ("subtraction = ${a-b}");
  print ("Multiplication = ${a*b}");
  print ("Modules = ${a%b}");
  print("division = ${a/b}");

  // Unary Operator
  int x = 101;
  // x =x+a
  print(x++);
  print(x);
  // x = x-a
  print(x--);
  print(x);
  print(--x);
  print(++x);

  // Assignment Operator
  double z = 100;
  z+=10;
  z-=10;
  z*=2;
  z/=10;
  z%=8;
  


}